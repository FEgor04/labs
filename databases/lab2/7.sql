SELECT DISTINCT COUNT("ЧЛВК_ИД")
FROM "Н_ЛЮДИ"
         INNER JOIN "Н_ВЕДОМОСТИ" on "Н_ЛЮДИ"."ИД" = "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
WHERE "ОЦЕНКА" = '3'
  AND EXISTS(SELECT "Н_УЧЕНИКИ"."ИД"
             FROM "Н_УЧЕНИКИ"
                      INNER JOIN "Н_ПЛАНЫ" НП on "Н_УЧЕНИКИ"."ПЛАН_ИД" = НП."ИД"
                      INNER JOIN "Н_ОТДЕЛЫ" НО on НП."ОТД_ИД" = НО."ИД"
             WHERE "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
               AND НО."КОРОТКОЕ_ИМЯ" = 'КТиУ')