SELECT 
  NEG_ID, 
  S.NAME AS NAME, 
  A.DESCRIPTION, 
  C.ID AS CAGE_ID, 
  T.DESCRIPTION AS CAGE_LOOKS_LIKE, 
  CREATURES.NAME 
FROM 
  ACTIONS AS A 
  LEFT JOIN ACTION_SUBJECT AS ASUB ON A.ID = ASUB.ACTION_ID 
  LEFT JOIN SCIENTISTS AS S ON ASUB.SUBJECT_ID = S.ID 
  LEFT JOIN NEGOTIATION_ACTIONS AS NA ON A.ID = NA.ACTION_ID 
  LEFT JOIN NEGOTIATIONS AS N ON NA.NEG_ID = N.ID 
  INNER JOIN ACTION_CAGE_OBJECT AS ACO ON A.ID = ACO.ACTION_ID 
  INNER JOIN CAGES AS C ON ACO.CAGE_ID = C.ID 
  INNER JOIN CAGE_LOOKS_LIKE AS CLL ON C.ID = CLL.CAGE_ID 
  LEFT JOIN THINGS AS T ON CLL.THING_ID = T.ID 
  LEFT JOIN CREATURES ON C.CREATURE_ID = CREATURES.ID 
WHERE 
  S.ID = 2 AND A.ID=1;

