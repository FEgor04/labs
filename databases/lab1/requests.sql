SELECT 
  NEG_ID, 
  S.NAME AS NAME, 
  A.DESCRIPTION, 
  CAGES.ID AS CAGE_ID, 
  T.DESCRIPTION AS CAGE_COVERED_WITH, 
  CREATURES.NAME 
FROM 
  ACTIONS AS A 
  LEFT JOIN ACTION_SUBJECT AS ASUB ON A.ID = ASUB.ACTION_ID 
  LEFT JOIN SCIENTISTS AS S ON ASUB.SUBJECT_ID = S.ID 
  LEFT JOIN NEGOTIATION_ACTIONS AS NA ON A.ID = NA.ACTION_ID 
  LEFT JOIN NEGOTIATIONS AS N ON NA.NEG_ID = N.ID 
  INNER JOIN ACTION_THING_OBJECT AS ATO ON A.ID = ATO.ACTION_ID 
  INNER JOIN THINGS AS T ON T.ID = ATO.THING_ID
  INNER JOIN CAGE_COVERED_WITH AS CCW ON CCW.THING_ID = T.ID
  INNER JOIN CAGES ON CCW.CAGE_ID = CAGES.ID
  INNER JOIN CREATURES ON CAGES.CREATURE_ID = CREATURES.ID;

