SELECT 
  N.ID AS NEG_ID, 
  S.ID AS SCIENTIST, 
  S.NAME, 
  F.ID AS FUND_ID 
FROM 
  NEGOTIATIONS AS N 
  LEFT JOIN NEGOTIATIONS_PARTICIPANTS AS NP ON N.ID = NP.NEG_ID 
  LEFT JOIN SCIENTISTS AS S ON NP.SCIENTIST_ID = S.ID 
  INNER JOIN FUNDS AS F ON F.OWNER = S.ID;

